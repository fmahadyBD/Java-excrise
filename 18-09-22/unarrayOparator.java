public class unarrayOparator{
    public static void main(String[]args){
        int a=10;
        System.out.println("a=10");
      
        System.out.println("a++ is: " + a++);
        System.out.println("a is:" + a);
        System.out.println("++a is:" + ++a);
        System.out.println("a is:" + a);

        System.out.println("a-- is:" + a--);
        System.out.println("a is:" + a);
        System.out.println("--a is:" + --a);
        System.out.println("a is:" + a);
      

    }
}